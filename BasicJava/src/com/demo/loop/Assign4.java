package com.demo.loop;

public class Assign4 {


	public static void main(String[] args) {
		        System.out.println("Numbers from 1 to 10, stopping at 5:");

		        for (int i = 1; i <= 10; i++) {
		            System.out.print(i + " ");
		            if (i == 5) {
		                break; // Exit the loop when i reaches 5
		            }
		        }
		    }
		

	}


